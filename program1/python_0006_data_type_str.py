str1 = "Hello Python!"
str2 = 'I am a str value, surrounded by single quote.'
str3 = "I am another str value, surrounded by double quotes."

print('variable str1 type is:', type(str1), 'str1=', str1)
print('variable str2 type is:', type(str2), 'str2=', str2)
print('variable str3 type is:', type(str3), 'str3=', str3)


first_name = 'Tom'
last_name = 'Hanks'

# '+' sign joins str variables together and get a new str value
full_name = first_name + " " + last_name

print('My favourite actor is: ', full_name)

# '*' sign duplicates str and get a new str
separate_line = '---*' * 50
print(separate_line)